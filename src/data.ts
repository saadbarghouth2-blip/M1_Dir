// Generate realistic simulated 24-hour traffic count history for every direction
// peaking around 08:00 (morning peak) and 17:00 (evening peak)
export const generateTrafficData = (): TrafficCount[] => {
  const records: TrafficCount[] = [];
  
  // Base hourly multiplier factors
  const hourlyFactors = [
    0.15, 0.10, 0.08, 0.12, 0.25, 0.45, 0.75, 0.95, // 0-7
    1.00, 0.85, 0.70, 0.65, 0.60, 0.65, 0.70, 0.78, // 8-15
    0.90, 0.98, 0.85, 0.65, 0.50, 0.38, 0.28, 0.20  // 16-23
  ];

  // Base capacities/flows per direction
  directions.forEach((d) => {
    // Generate distinct volumes based on route length or code properties
    const baseVolume = Math.round(150 + (d.length * 1.5) + (parseInt(d.member_code.replace(/\D/g, "")) || 2) * 25);
    
    for (let hour = 0; hour < 24; hour++) {
      const factor = hourlyFactors[hour];
      // Add minor random noise (up to 15%)
      const randomNoise = 0.92 + Math.random() * 0.16;
      const totalVolume = Math.round(baseVolume * factor * randomNoise);
      
      // Calculate vehicle class splits
      const carPercentage = 0.72 + (Math.random() * 0.08);
      const truckPercentage = 0.08 + (Math.random() * 0.05);
      const busPercentage = 0.05 + (Math.random() * 0.04);
      const motoPercentage = 1 - (carPercentage + truckPercentage + busPercentage);
      
      const cars = Math.round(totalVolume * carPercentage);
      const trucks = Math.round(totalVolume * truckPercentage);
      const buses = Math.round(totalVolume * busPercentage);
      const motorcycles = Math.max(0, totalVolume - (cars + trucks + buses));

      // Speed simulation (lower speeds during peak hours due to congestion)
      const baseSpeed = 65 - (d.length / 15);
      const currentSpeed = Math.round(baseSpeed * (1 - (factor * 0.35)) + (Math.random() * 6 - 3));

      records.push({
        id: `${d.id}-${hour}`,
        junctionCode: d.member_code,
        directionId: d.id,
        dirString: d.dir,
        timestamp: `${hour.toString().padStart(2, "0")}:00`,
        hour,
        volume: totalVolume,
        vehicleBreakdown: {
          [VehicleClass.CAR]: cars,
          [VehicleClass.TRUCK]: trucks,
          [VehicleClass.BUS]: buses,
          [VehicleClass.MOTORCYCLE]: motorcycles,
        },
        speed: currentSpeed
      });
    }
  });

  return records;
};

